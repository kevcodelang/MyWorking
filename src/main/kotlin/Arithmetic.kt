fun main() {
    var a : Int = 3
    var b : Int = 2
    println("a = " + a + " b = " + b)
    println("a + b = ${a + b}")
    println("a - b = ${a - b}")
    println("a * b = ${a * b}")
    println("a / b = ${a / b}")

    a += 5
    println("The new value of a is $a because i add 5 to increase a")
}